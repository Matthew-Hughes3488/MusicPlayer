// Helper function to format duration
export const formatDuration = (seconds) => {
  const minutes = Math.floor(seconds / 60);
  const remainingSeconds = seconds % 60;
  return `${minutes}:${remainingSeconds.toString().padStart(2, '0')}`;
};

// Helper function to get tracks by genre
export const getTracksByGenre = (genre) => {
  return dummyTracks.filter(track => track.genre.toLowerCase() === genre.toLowerCase());
};

// Helper function to search tracks
export const searchTracks = (query) => {
  const searchTerm = query.toLowerCase();
  return dummyTracks.filter(track => 
    track.title.toLowerCase().includes(searchTerm) ||
    track.artist.toLowerCase().includes(searchTerm) ||
    track.album.toLowerCase().includes(searchTerm) ||
    track.genre.toLowerCase().includes(searchTerm)
  );
};